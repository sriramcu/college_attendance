def special_print(final_list):
    #To print the list generated by dict_union
    final_str = ''
    for inner in final_list:
        for element in inner:
            if type(element) == datetime.date:  
                #I don't want to print date in yy-mm-dd as is default in the __str__ attribute of datetime type
                #print(element.strftime('%d-%m-%y'),end = '\t')
                final_str = final_str + element.strftime('%d-%m-%y') + '\t'
                
            else:
                #print(element,end = '\t')
                final_str = final_str + element.ljust(22)
        #print('\n')
        final_str = final_str + '\n'
    return final_str

def nestrepl(lst, what, repl):    
    #to recursively(deep) replace all occurrences of the element 'what' with the element 'repl' in a nested list 'lst', modifies lst without any return value
    
    for index, item in enumerate(lst):
        if type(item) == list:
            nestrepl(item, what, repl)
        else:
            if item == what:
                lst[index] = repl
              
def substract(a, b):    
    #to return a-b where a and b are two strings without modifying a and b                         
    return "".join(a.rsplit(b))
    
def substitute(my_list,old_element,new_element):
    #Stupid version of nestrepl; shallow replaces in a new list leaving the original list unmodified
    my_new_list = [new_element if x==old_element else x for x in my_list]
    return my_new_list

