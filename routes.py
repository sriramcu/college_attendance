def insert_position(position, list1, list2):
    return list1[:position] + list2 + list1[position:]
    
def dict_union(a):
    ret_list = []
    d = {}
    for sl in a:
        sl_tuple = (sl[0],sl[-1])
        
        if sl_tuple not in d.keys():
            d[sl_tuple] = []
        nl = sl[1:-1]
        d[sl_tuple].append(nl)
    for key,similar in d.items():
        concat_list = [j for i in similar for j in i]
        d[key] = concat_list
        main_list = list(key)
        main_list = insert_position(1,main_list,concat_list)
        ret_list.append(main_list)
        
    return ret_list
    
    

    
