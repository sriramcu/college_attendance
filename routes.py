def date_increment(date_str):
    #converts date string into date object; increments it using timedelta; converts it back to a string and returns it
    dt_obj = datetime.datetime.strptime(date_str,'%d-%m-%Y').date()
    if dt_obj.strftime('%A')=='Friday':
        new_dt_obj = dt_obj + timedelta(days=3)
    else:
        new_dt_obj = dt_obj + timedelta(days=1)   
    return new_dt_obj.strftime('%d-%m-%Y')
    
